DROP TABLE IF EXISTS PRODUCT;

CREATE TABLE PRODUCT (
  PRODUCT_ID INT AUTO_INCREMENT  PRIMARY KEY,
  PRODUCT_NAME VARCHAR(50) NOT NULL,
  PRODUCT_PRICE NUMERIC(10,2) NOT NULL,
);
 
DROP TABLE IF EXISTS SALE_ORDER;

CREATE TABLE SALE_ORDER (
  SALE_ORDER_ID INT AUTO_INCREMENT  PRIMARY KEY,
  SALE_ORDER_DATE TIMESTAMP NOT NULL,
  SALE_ORDER_DISCOUNT_TOTAL NUMERIC(10,2) NOT NULL,
  SALE_ORDER_TOTAL NUMERIC(10,2) NOT NULL,
);

DROP TABLE IF EXISTS SALE_ORDER_PRODUCT;

CREATE TABLE SALE_ORDER_PRODUCT (
  SALE_ORDER_PRODUCT_PRODUCT_ID INT,
  SALE_ORDER_PRODUCT_SALE_ORDER_ID INT,
  PRIMARY KEY (SALE_ORDER_PRODUCT_PRODUCT_ID, SALE_ORDER_PRODUCT_SALE_ORDER_ID),
  foreign key (SALE_ORDER_PRODUCT_SALE_ORDER_ID) references SALE_ORDER(SALE_ORDER_ID),
  foreign key (SALE_ORDER_PRODUCT_PRODUCT_ID) references PRODUCT(PRODUCT_ID)
);
